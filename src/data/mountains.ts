/**
 * ランダムに問題セットを生成する
 */
export function generateQuiz(count = 10): Mountain[] {
  const shuffled = [...mountains].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

/**
 * 正解以外の選択肢を生成する
 */
export function generateOptions(correct: Mountain, optionCount = 4): Mountain[] {
  const others = mountains.filter(m => m.id !== correct.id);
  const shuffled = others.sort(() => Math.random() - 0.5);
  const options = [correct, ...shuffled.slice(0, optionCount - 1)];
  return options.sort(() => Math.random() - 0.5);
}
